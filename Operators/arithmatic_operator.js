
let a = 10;
let b = 20;

// addition

console.log(a + b);

// subtraction

console.log(a - b)

// multiply
console.log(a * b)

// division

console.log(a / b)

// module operator give reminder

console.log(a % b)
//exponential operator
console.log(a ** b)

// increment operator
console.log(++a); // 11
let c = a++; // c will be 11 because assignment will happen first then increment
console.log(c)

console.log(--b); // 19
let d = b--; // d= 19 because assignment happen first

console.log(d)